package com.example.listview;

public class ListData {
    String name, time, peng;
    int ingredients, desc;
    int image;

    public ListData(String name, String time, String peng, int ingredients, int desc, int image) {
        this.name = name;
        this.time = time;
        this.peng = peng;
        this.ingredients = ingredients;
        this.desc = desc;
        this.image = image;
    }
}